# comparison operator

# == - equal
# != - not equal
# > - greater than
# < - less than
# >= - greater than equal to
# <= - less than equal to

num_1 = 50 
num_2 = 50

# ==
equal = num_1 == num_2
print("Equal : ",equal)

# !=
not_equal = num_1 != num_2
print("Not Equal : ",not_equal)

# >
greater_than = num_1 > num_2
print("Greater than : ",greater_than)

# <
less_than = num_1 < num_2
print("Less than : ",less_than)

# >=
greater_than_equal_to = num_1 >= num_2
print("Greater than equal to : ",greater_than_equal_to)

# <=
less_than_equal_to = num_1 <= num_2
print("Less than equal to: ",less_than_equal_to)