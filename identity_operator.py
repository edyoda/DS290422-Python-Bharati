# Identity Operator

# is - returns True if both the variables are the same object , it works like '=='
# is not - returns True if both the variables are not the same object


num1 = 10
num2 = 10
is_equal = num1 is num2 
print(is_equal)

num1 = 10
num2 = 20
is_equal = num1 is not num2 
print(is_equal)