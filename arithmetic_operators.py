# arithmetic operators

# +
# -
# * 
# /
# % - Modulus
# ** - Exponentia
# Floor Division - //

# Addition (+)
no_1 = 20
no_2 = 30
add = no_1 + no_2
print("Addition of",no_1,"and",no_2,"is",add)

# Subtraction (-)
no_1 = 30
no_2 = 40
sub = no_1 - no_2
print("Subtraction of",no_1,"and",no_2,"is",sub)

# Multiplication (*)
no_1 = 30
no_2 = 20
multi = no_1 * no_2
print("Multiplication of",no_1,"and",no_2,"is",multi)

# Division (/) - returns quotient and it always returns float value
no_1 = 10
no_2 = 5
div = no_1 / no_2
print("Divison of",no_1,"and",no_2,"is",div)

# Modulus (%) - returns remainder
no_1 = 26
no_2 = 9
mod = no_1 % no_2
print("Modulus of",no_1,"and",no_2,"is",mod)

# Exponentia (**) - power
no_1 = 2
no_2 = 2
expo = no_1 ** no_2
print("Modulus of",no_1,"and",no_2,"is",expo)

# Floor Division (//) - converts the float value into int which is returned by /
no_1 = 10
no_2 = 4
div = no_1 / no_2
print("Division of",no_1,"and",no_2,"is",div)

floor_div = no_1 // no_2
print("Floor Division of",no_1,"and",no_2,"is",floor_div)

