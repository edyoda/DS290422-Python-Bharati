# +=
num1 = 10
num2 = 20
print("num1 before addition : ",num1)
num1 += num2   # num1 = num1 + num2
print("num1 after addition : ",num1)


# -=
num1 = 10
num2 = 20
print("num1 before subtraction : ",num1)
num1 -= num2   # num1 = num1 - num2
print("num1 after subtraction : ",num1)


# **=
num1 = 10
num2 = 20
print("num1 before exponentia : ",num1)
num1 **= num2   # num1 = num1 - num2
print("num1 after exponentia : ",num1)


#- //=
num1 = 10
num2 = 20
print("num1 before floor division : ",num1)
num1 //= num2   # num1 = num1 // num2
print("num1 after floor division : ",num1)


# /=
num1 = 10
num2 = 20
print("num1 before division : ",num1)
num1 /= num2   # num1 = num1 / num2
print("num1 after division : ",num1)


